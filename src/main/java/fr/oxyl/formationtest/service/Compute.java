package fr.oxyl.formationtest.service;

public class Compute {

    public int add2Numbers(int a, int b) {
        return a + b;
    }

    public int substract2Numbers(int a, int b) {
        return a - b;
    }

    public int multiply2Numbers(int a, int b) {
        return a * b;
    }

    public int divide2Numbers(int a, int b) {
        return a / b;
    }

    public boolean isGreaterThan(int a, int b) {
        return a > b;
    }
}
